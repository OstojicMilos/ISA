package model;

public class FriendshipWrapper {
	
	private User first;
	private User second;
	
	public FriendshipWrapper() {
		super();
	}
	
	public User getFirst() {
		return first;
	}
	public void setFirst(User first) {
		this.first = first;
	}
	public User getSecond() {
		return second;
	}
	public void setSecond(User second) {
		this.second = second;
	}
	
	
	
}
